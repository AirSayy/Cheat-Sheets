// Data types in JavaScript

// STRINGS is used to store text

const myString = 'hello world';

// NUMBERS represents integer and floating numbers (decimals and exponentials).

const numb1 = 7
const numb2 = 10
console.log(numb1) // 7

// BOOLEAN represents one of two values: true or false

const isTrue = true
const isFalse = false
console.log(isFalse) //false

// NULL is a special value that represents empty or unknown value

const unknown = null
console.log (unknown) //null

// UNDEFINED If a variable is declared but the value is not assigned, then the value of that variable will be undefined

let student;
const student2 = undefined
console.log(student) //undefined


// OBJECTS An object is a complex data type that allows us to store collections of data

const user = {
    firstName: 'john',
    lastName: null,
    age: 10
};
console.log(user)// john , null , 10

